// Settings of this program
public class settings {
    public static boolean showHud = true; // true = Show HUD, false = Don't show HUD
    public static boolean showTitle = showHud && true; // true = Show title, false = Don't show title
    public static boolean showBigTitle = true; // true = Big title, false = Small title
    public static boolean showInfo = showHud && true; // true = Show info, false = Don't show info
    public static boolean abilityChangeKeys = true; // true = Ability to change keys, false = No ability to change keys
    public static boolean clearTerminal = true; // true = Clear terminal, false = Don't clear terminal
}